import UIKit

extension UIColor {
    static var accent: UIColor  { return UIColor(named: "AccentColor")! }
    static var primary: UIColor  { return UIColor(named: "PrimaryColor")! }
    static var whiteColor: UIColor  { return UIColor(named: "WhiteColor")! }
    static var darkBackgroundColor: UIColor  { return UIColor(named: "DarkBackgroundColor")! }
    static var blackColor: UIColor  { return UIColor(named: "BlackColor")! }
}
